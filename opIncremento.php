<?php
/**
 * Operadores de incremento e decremento
 * 
 * Servem para somar ou subtrair em (+1 ou -1) qualquer valor do tipo numérico ou string
 */

 $valor = 20;

 //pré-incremento
 echo ++$valor;

 //Pós-incremento
 echo $valor++;

 echo '<hr>';

 //pré-descremento
 echo --$valor;

 //pos-decremento
 echo $valor--;